'use client';

import { HttpError } from '@rehua/sdk';
import { QueryClient, QueryClientProvider } from '@tanstack/react-query';
import { ReactQueryDevtools } from '@tanstack/react-query-devtools';
import { createContext, useEffect, type JSX, type ReactNode } from 'react';
import { TypeGuardError } from 'typia';
import { useLocalStorage, useIsClient } from 'usehooks-ts';

export const MAX_RETRIES = 3;

export const APIUrlContext = createContext<string>('');
export const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      retry: (failureCount, error): boolean => {
        // Disable retries for TypeGuardError
        // TypeGuardError is generated by @rehua/sdk and typia type checking
        if (error instanceof TypeGuardError) {
          return false;
        }

        if (error instanceof HttpError) {
          switch (error.status) {
            case 401:
              return false; // Unauthorized, do not retry
            case 403:
              return false; // Forbidden, do not retry
            case 404:
              return false; // Not Found, do not retry
            default:
              break;
          }
        }

        return failureCount < MAX_RETRIES;
      },
    },
  },
});

export default function Providers({
  children,
}: Readonly<{ children: ReactNode }>): JSX.Element {
  const isClient = useIsClient();
  const [apiUrl, setApiUrl] = useLocalStorage<string>('apiUrl', '', {
    serializer: (d) => d,
    deserializer: (d) => d,
  });

  useEffect(() => {
    if (apiUrl.trim() === '') {
      const url = new URL('/api', globalThis.location.href);
      setApiUrl(url.href.replace(/\/$/, ''));
    }

    if (apiUrl.trim() !== apiUrl) {
      setApiUrl((s) => s.trim());
    }

    if (apiUrl.endsWith('/')) {
      setApiUrl((s) => s.replace(/\/$/, ''));
    }
  }, [apiUrl, setApiUrl]);

  if (!isClient || apiUrl.trim() === '') {
    return <h1>Loading...</h1>;
  }

  return (
    <QueryClientProvider client={queryClient}>
      <APIUrlContext value={apiUrl}>{children}</APIUrlContext>
      <ReactQueryDevtools />
    </QueryClientProvider>
  );
}
